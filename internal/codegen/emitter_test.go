func TestEmitModelFile_SimpleModel(t *testing.T) {
	m := ModelInfo{
		Name:      "Product",
		PkgPath:   "testmod/models",
		PkgName:   "models",
		PKType:    "int",
		TableName: "products",
		Fields: []FieldInfo{
			{Name: "name", GoType: "string", ColumnName: "name", LocalGoType: "string"},
			{Name: "price", GoType: "int", ColumnName: "price", LocalGoType: "int"},
			{Name: "inStock", GoType: "bool", ColumnName: "in_stock", LocalGoType: "bool"},
		},
	}

	out := EmitModelFile(m)

	// Header
	assert.Contains(t, out, "// Code generated by drel. DO NOT EDIT.")
	assert.Contains(t, out, "package models")
	assert.Contains(t, out, `"github.com/alternayte/drel"`)

	// Column references struct
	assert.Contains(t, out, "var Products = struct {")
	assert.Contains(t, out, "ID drel.OrderedColumn[int]")
	assert.Contains(t, out, "Name drel.StringColumn")
	assert.Contains(t, out, "Price drel.OrderedColumn[int]")
	assert.Contains(t, out, "InStock drel.BoolColumn")
	assert.Contains(t, out, `Name: drel.NewStringCol("name")`)
	assert.Contains(t, out, `Price: drel.NewOrderedCol[int]("price")`)
	assert.Contains(t, out, `InStock: drel.NewBoolCol("in_stock")`)

	// Scan function
	assert.Contains(t, out, "func scanProduct(row drel.Row) (*Product, error)")
	assert.Contains(t, out, "idPtr, createdAtPtr, updatedAtPtr := p.ScanPtrs()")
	assert.Contains(t, out, "idPtr")
	assert.Contains(t, out, "&p.name")
	assert.Contains(t, out, "&p.price")
	assert.Contains(t, out, "&p.inStock")
	assert.Contains(t, out, "createdAtPtr")
	assert.Contains(t, out, "updatedAtPtr")

	// Snapshot
	assert.Contains(t, out, "type productSnapshot struct {")
	assert.Contains(t, out, "func snapshotProduct(p *Product) any")
	assert.Contains(t, out, "name: p.name")
	assert.Contains(t, out, "price: p.price")

	// Diff
	assert.Contains(t, out, "func diffProduct(p *Product, snap any) []drel.FieldChange")
	assert.Contains(t, out, `s := snap.(productSnapshot)`)
	assert.Contains(t, out, `Column: "name", Value: p.name`)

	// PK value
	assert.Contains(t, out, "func productPKValue(p *Product) any")
	assert.Contains(t, out, "return p.ID()")

	// Insert columns
	assert.Contains(t, out, "func productInsertColumns(p *Product) ([]string, []any)")
	assert.Contains(t, out, `"name", "price", "in_stock"`)
	assert.Contains(t, out, "p.name, p.price, p.inStock")

	// Scan returning
	assert.Contains(t, out, "func productScanReturning(p *Product, row drel.Row) error")
	assert.Contains(t, out, "idPtr, createdAtPtr, updatedAtPtr := p.ScanPtrs()")
	assert.Contains(t, out, "row.Scan(idPtr, createdAtPtr, updatedAtPtr)")

	// ModelMeta
	assert.Contains(t, out, "var ProductMeta = drel.ModelMeta[Product]{")
	assert.Contains(t, out, `Table:   "products"`)
	assert.Contains(t, out, `Columns: []string{"id", "name", "price", "in_stock", "created_at", "updated_at"}`)
	assert.Contains(t, out, `PKColumn: "id"`)
	assert.Contains(t, out, "Scan:          scanProduct,")
	assert.Contains(t, out, "Snapshot:      snapshotProduct,")
	assert.Contains(t, out, "Diff:          diffProduct,")
	assert.Contains(t, out, "PKValue:       productPKValue,")
	assert.Contains(t, out, "InsertColumns: productInsertColumns,")
	assert.Contains(t, out, "ScanReturning: productScanReturning,")
}

func TestEmitModelFile_WithSoftDelete(t *testing.T) {
	m := ModelInfo{
		Name:          "Article",
		PkgPath:       "testmod/models",
		PkgName:       "models",
		PKType:        "int",
		TableName:     "articles",
		HasSoftDelete: true,
		Fields: []FieldInfo{
			{Name: "title", GoType: "string", ColumnName: "title", LocalGoType: "string"},
		},
	}

	out := EmitModelFile(m)

	// Verify deletedAt in scan
	assert.Contains(t, out, "p.DeletedAtPtr()")

	// Verify deleted_at column in column refs
	assert.Contains(t, out, `DeletedAt drel.Column[*time.Time]`)
	assert.Contains(t, out, `DeletedAt: drel.NewCol[*time.Time]("deleted_at")`)

	// Verify deleted_at in Columns list
	assert.Contains(t, out, `"deleted_at"`)

	// Verify scan order: fields, then softDelete, then createdAt/updatedAt
	scanLine := extractLine(out, "row.Scan(")
	assert.True(t, strings.Index(scanLine, "DeletedAtPtr") < strings.Index(scanLine, "createdAtPtr"),
		"deletedAt should be scanned before createdAt")
}


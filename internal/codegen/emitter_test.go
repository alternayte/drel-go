func TestColumnType(t *testing.T) {
	tests := []struct {
		goType   string
		wantType string
		wantCtor string
	}{
		{"int", "drel.OrderedColumn[int]", `drel.NewOrderedCol[int]("col")`},
		{"int64", "drel.OrderedColumn[int64]", `drel.NewOrderedCol[int64]("col")`},
		{"float64", "drel.OrderedColumn[float64]", `drel.NewOrderedCol[float64]("col")`},
		{"string", "drel.StringColumn", `drel.NewStringCol("col")`},
		{"bool", "drel.BoolColumn", `drel.NewBoolCol("col")`},
		{"time.Time", "drel.Column[time.Time]", `drel.NewCol[time.Time]("col")`},
	}
	for _, tt := range tests {
		t.Run(tt.goType, func(t *testing.T) {
			assert.Equal(t, tt.wantType, columnTypeName(tt.goType))
			assert.Equal(t, tt.wantCtor, columnConstructor(tt.goType, "col"))
		})
	}
}


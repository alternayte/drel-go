package codegen

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"

	"golang.org/x/tools/go/packages"
)

// generatedHeader marks files written by drel; only files starting with it are
// eligible for stale-file cleanup.
const generatedHeader = "// Code generated by drel. DO NOT EDIT."

// emitted holds the formatted content destined for a single output file.
type emitted struct {
	path    string
	content string
}

// ResolveModuleRoot finds the Go module root (the nearest ancestor directory
// containing go.mod) by walking up from startDir. It falls back to startDir when
// no go.mod is found, preserving the prior behaviour for configs that already sit
// at the module root.
func ResolveModuleRoot(startDir string) string {
	dir := startDir
	for {
		if fi, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil && !fi.IsDir() {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return startDir
		}
		dir = parent
	}
}

// Generate runs the full code generation pipeline atomically: it loads config,
// cleans stale generated files (so the subsequent scan is unobstructed), scans
// packages, validates the model set, emits and gofmts every output file in
// memory, and only then writes anything to disk (write-to-temp + rename). A
// failure at any step before the write phase leaves the working tree untouched.
// After writing, stale generated *_drel.go files that no longer back a scanned
// model are removed.
func Generate(configPath string) error {
	cfg, err := LoadConfig(configPath)
	if err != nil {
		return err
	}

	// Resolve the config file's directory as the working directory for scanning.
	cfgDir := filepath.Dir(configPath)
	if !filepath.IsAbs(cfgDir) {
		abs, err := filepath.Abs(cfgDir)
		if err != nil {
			return fmt.Errorf("codegen: resolve config dir: %w", err)
		}
		cfgDir = abs
	}

	// Pre-scan stale cleanup: remove *_drel.go files from the package directories
	// before scanning so that stale generated files (referencing deleted types) do
	// not cause package load errors. We need only the file-list mode of
	// packages.Load to discover directories without type-checking.
	if err := removeStaleInDirs(cfg.Packages, cfgDir); err != nil {
		return err
	}

	// Patterns in drel.yaml (e.g. ./models) are resolved relative to the
	// directory that contains the config file. go/packages.Load discovers the
	// module root on its own from the working directory, so cfgDir is the
	// correct base — NOT ResolveModuleRoot(cfgDir), which would resolve
	// ./models relative to the repo root when the config lives in a subdir.
	models, err := ScanPackages(cfg.Packages, cfgDir)
	if err != nil {
		return err
	}

	if len(models) == 0 {
		return fmt.Errorf("codegen: no models found in packages %v", cfg.Packages)
	}

	// Validate the whole model set before touching disk: duplicate DB field
	// names, unresolved relation targets, and column-less models all fail here
	// so a bad input never leaves a half-generated tree.
	if err := ValidateModels(models); err != nil {
		return err
	}

	// --- Emit + format everything into memory (no disk writes yet). ---
	var files []emitted
	intended := make(map[string]bool) // per-model output paths we will write

	for _, m := range models {
		content, err := EmitModelFileChecked(m)
		if err != nil {
			return err
		}
		formatted, err := formatGenerated(content)
		if err != nil {
			return fmt.Errorf("codegen: format model %q: %w", m.Name, err)
		}
		outPath := filepath.Join(m.Dir, strings.ToLower(m.Name)+"_drel.go")
		files = append(files, emitted{path: outPath, content: formatted})
		intended[outPath] = true
	}

	dbPath := cfg.Output.DB
	if !filepath.IsAbs(dbPath) {
		dbPath = filepath.Join(cfgDir, dbPath)
	}
	dbPkgName := filepath.Base(filepath.Dir(dbPath))
	dbContent, err := formatGenerated(EmitDBFile(models, dbPkgName))
	if err != nil {
		return fmt.Errorf("codegen: format db file %s: %w", dbPath, err)
	}
	files = append(files, emitted{path: dbPath, content: dbContent})

	// --- Write phase: every file emitted and formatted successfully. ---
	for _, f := range files {
		if err := atomicWrite(f.path, f.content); err != nil {
			return fmt.Errorf("codegen: write %s: %w", f.path, err)
		}
	}

	// --- Remove stale generated *_drel.go files no longer backing a model. ---
	if err := removeStaleModelFiles(models, intended); err != nil {
		return err
	}

	return nil
}

// formatGenerated gofmt-formats generated source. It performs no disk I/O; on a
// formatting error nothing is written by the caller and the error is returned.
func formatGenerated(content string) (string, error) {
	formatted, err := format.Source([]byte(content))
	if err != nil {
		return "", err
	}
	return string(formatted), nil
}

// atomicWrite writes content to path via a temp file in the same directory
// followed by os.Rename, so a reader never sees a partially-written file.
func atomicWrite(path, content string) error {
	dir := filepath.Dir(path)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	tmp, err := os.CreateTemp(dir, ".drel-*.tmp")
	if err != nil {
		return err
	}
	tmpName := tmp.Name()
	if _, err := tmp.WriteString(content); err != nil {
		tmp.Close()
		os.Remove(tmpName)
		return err
	}
	if err := tmp.Close(); err != nil {
		os.Remove(tmpName)
		return err
	}
	if err := os.Chmod(tmpName, 0644); err != nil {
		os.Remove(tmpName)
		return err
	}
	if err := os.Rename(tmpName, path); err != nil {
		os.Remove(tmpName)
		return err
	}
	return nil
}

// removeStaleInDirs removes drel-generated *_drel.go files from the directories
// of the configured package patterns before scanning. This prevents stale
// generated files (that reference deleted model types) from causing package load
// errors in the subsequent ScanPackages call.
func removeStaleInDirs(patterns []string, dir string) error {
	cfg := &packages.Config{
		Mode: packages.NeedFiles,
		Dir:  dir,
	}
	pkgs, err := packages.Load(cfg, patterns...)
	if err != nil {
		// If we can't load even file lists, proceed; the main scan will report the error.
		return nil
	}
	for _, pkg := range pkgs {
		if len(pkg.GoFiles) == 0 {
			continue
		}
		pkgDir := filepath.Dir(pkg.GoFiles[0])
		entries, err := os.ReadDir(pkgDir)
		if err != nil {
			continue
		}
		for _, e := range entries {
			if e.IsDir() || !strings.HasSuffix(e.Name(), "_drel.go") {
				continue
			}
			path := filepath.Join(pkgDir, e.Name())
			data, err := os.ReadFile(path)
			if err != nil {
				continue
			}
			if !strings.HasPrefix(string(data), generatedHeader) {
				continue // hand-written file
			}
			if err := os.Remove(path); err != nil {
				return fmt.Errorf("codegen: remove stale %s: %w", path, err)
			}
		}
	}
	return nil
}

// removeStaleModelFiles deletes drel-generated *_drel.go files in the scanned
// package directories that are not in the intended set (their backing model was
// deleted or renamed). Only files carrying the generated header are removed.
func removeStaleModelFiles(models []ModelInfo, intended map[string]bool) error {
	seenDirs := make(map[string]bool)
	for _, m := range models {
		if m.Dir == "" || seenDirs[m.Dir] {
			continue
		}
		seenDirs[m.Dir] = true

		entries, err := os.ReadDir(m.Dir)
		if err != nil {
			return fmt.Errorf("codegen: scan dir %s for stale files: %w", m.Dir, err)
		}
		for _, e := range entries {
			if e.IsDir() || !strings.HasSuffix(e.Name(), "_drel.go") {
				continue
			}
			path := filepath.Join(m.Dir, e.Name())
			if intended[path] {
				continue
			}
			data, err := os.ReadFile(path)
			if err != nil {
				return fmt.Errorf("codegen: read %s: %w", path, err)
			}
			if !strings.HasPrefix(string(data), generatedHeader) {
				continue // hand-written file that happens to match the suffix
			}
			if err := os.Remove(path); err != nil {
				return fmt.Errorf("codegen: remove stale %s: %w", path, err)
			}
		}
	}
	return nil
}
